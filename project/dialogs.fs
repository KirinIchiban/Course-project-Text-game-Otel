let startDialog characterName : Dialog<unit> = dialog {
    let! state = getState
    let newTalkCount = 
        match Map.tryFind characterName state.TalkedTo with
        | Some count -> count + 1
        | None -> 1
    let newRequiredTalks = 
        if List.contains characterName Characters then
            add characterName state.RequiredTalks
        else state.RequiredTalks
    do! updateState (fun s -> 
        { s with 
            TalkedTo = Map.add characterName newTalkCount s.TalkedTo
            DialogCount = s.DialogCount + 1
            RequiredTalks = newRequiredTalks })
}

let showDialogProgress : Dialog<unit> = dialog {
    let! state = getState
    let total = List.length Characters
    let talked = state.TalkedTo |> Map.toList |> List.length
    do! writeLine (sprintf "\n[Прогресс опроса: %d/%d]" talked total)
}

