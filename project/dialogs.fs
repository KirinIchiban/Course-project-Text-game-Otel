                 "Снежное чучело Хинкуса на крыше"; "Пропавшие золотые часы"]
                |> List.filter (fun c -> not (List.contains c state.CluesFound))
                |> String.concat ", "
            return ("Ещё не все улики найдены. Нужно собрать: " + missing)
        else
            do! updateState (fun s -> { s with AllCluesCollected = true })
            return "=== АНАЛИЗ УЛИК ===\n\n\
            Все улики собраны!\n\n\
            ВЫВОДЫ:\n\
            1. Убийца не призрак - были мокрые следы\n\
            2. Хинкус не убийца - его самого пытались убить\n\
            3. Олаф был не туристом - у него был специальный прибор\n\
            4. Убийство совершено с чудовищной силой - нужен очень сильный человек\n\
            5. Окно было открыто для отвода глаз - убийца вошел через дверь\n\n\
            ПОДОЗРЕВАЕМЫЕ:\n\
            - Алекс Сневар: сильный, знает все ходы в отеле\n\
            - Однорукий незнакомец: появился в ночь убийства\n\
            - Мозес: сильный, пьяный, непредсказуемый\n\
            - Симонэ: альпинист, очень сильный физически\n\
            - Кайса: имеет мотив, если Олаф её оскорбил\n\
            - Брюн: была в номере Олафа перед убийством\n\n\
            ...но улики противоречат друг другу. Нет ни одного человека, который...\n\
            ...Глебски садится на стул, прислонившись спиной к сейфу.\n\n\
            В голове проносятся слова:\n\
            \"Алекс: Дух Погибшего Альпиниста не покинул эти стены...\"\n\
            \"Хинкус: Он оборотень, колдун! Вельзевул среди нас!\"\n\
            \"Симонэ: Это не мистика, это научная фантастика!\"\n\n\
